function r.convertToTable(str:string)
    local function convert(x)
        if x == '0' then
            x = 0
        elseif x == '-0' then
            x = 180
        elseif x == '1' then
            x = 90
        elseif x == '-1' then
            x = -90
        else 
            x = 0
        end
        return (tonumber(x) or 0)
    end
    local x,y,z = str:match("([^,]*),?([^,]*),?([^,]*)")
    return {convert(x),convert(y),convert(z)}
end
